#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	a;
	int	b;

	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -nb;
	}
	if (nb >= 10)
	{
		b = nb / 10;
		ft_putchar(b + '0');
		a = nb % 10;
	}
	ft_putchar(a + '0');
}

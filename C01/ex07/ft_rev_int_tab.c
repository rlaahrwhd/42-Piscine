void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	b = 0;
	c = size - 1;
	while (a != (size / 2) + 1)
	{
		b = tab[a];
		tab[a] = tab[c];
		tab[c] = b;
		a++;
		c--;
	}
}
